package InterfaceAndAbstract;

public class PositiveOrNegative {
	public static void main (String args[]) {
		double num =12.300;
		
		if (num>0.0) {
			System.out.println("Number is positive");
		}
		else if (num<0.0) {
			System.out.println("Number is negative");
		}
		else {
			System.out.println("Number is 0");
		}
	}

}
